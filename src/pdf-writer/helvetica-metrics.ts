export function widthOfTextAtSize(text: string, fontSize: number): number {
    let total = 0;
    for (let i = 0; i < text.length; i++) {
        const cp = text.codePointAt(i)!;
        total += WIDTHS[cp] ?? DEFAULT_WIDTH;
    }
    return (total / 1000) * fontSize;
}

export function encodeWinAnsi(text: string): Uint8Array {
    const bytes: number[] = [];
    for (let i = 0; i < text.length; i++) {
        const cp = text.codePointAt(i)!;
        bytes.push(UNICODE_TO_WINANSI[cp] ?? (cp <= 255 ? cp : 63)); // 63 = '?'
    }
    return new Uint8Array(bytes);
}
